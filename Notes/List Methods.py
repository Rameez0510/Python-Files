# List methods

l = [1, 8,7,2,21,15]


l.sort() # sort in ascending order
print (l)

l.reverse() #reverse the order
print (l)

l.append(8) # adds the number at the end of the list
print (l)

l.insert(3, 8) # adds a number on a chosen index place 
print(l)

l.pop(6) # removes the number from the index place
print (l)

l.remove (21) # removes a mentioned number
print (l)
